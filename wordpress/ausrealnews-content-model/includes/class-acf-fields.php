                    'label' => 'Key Metrics',
                    'name'  => 'key_metrics',
                    'type'  => 'group',
                    'fields' => [
                        [
                            'key'          => 'field_median_price',
                            'label'        => 'Median Price',
                            'name'         => 'median_price',
                            'type'         => 'number',
                            'instructions' => 'Median property price in AUD.',
                            'required'     => 1,
                            'min'          => 0,
                        ],
                        [
                            'key'          => 'field_yoy_change',
                            'label'        => 'YoY Change (%)',
                            'name'         => 'yoy_change',
                            'type'         => 'number',
                            'instructions' => 'Year-on-year percentage change.',
                            'required'     => 0,
                            'min'          => -100,
                            'max'          => 100,
                            'step'         => 0.1,
                        ],
                        [
                            'key'          => 'field_vacancy_rate',
                            'label'        => 'Vacancy Rate (%)',
                            'name'         => 'vacancy_rate',
                            'type'         => 'number',
                            'instructions' => 'Current rental vacancy rate.',
                            'required'     => 0,
                            'min'          => 0,
                            'max'          => 100,
                            'step'         => 0.1,
                        ],
                        [
                            'key'          => 'field_days_on_market',
                            'label'        => 'Days on Market',
                            'name'         => 'days_on_market',
                            'type'         => 'number',
                            'instructions' => 'Average days property stays on market.',
                            'required'     => 0,
                            'min'          => 0,
                        ],
                    ],
                ],
                [
                    'key'          => 'field_source_urls_report',
                    'label'        => 'Source URLs',
                    'name'         => 'source_urls',
                    'type'         => 'url',
                    'instructions' => 'Data sources for this report.',
                    'multiple'     => true,
                ],
                [
                    'key'          => 'field_risk_level_report',
                    'label'        => 'Risk Level',
                    'name'         => 'risk_level',
                    'type'         => 'select',
                    'choices'      => ['Low' => 'Low', 'Medium' => 'Medium', 'High' => 'High'],
                    'default_value' => 'Low',
                ],
                [
                    'key'   => 'field_is_ai_generated_report',
                    'label' => 'Is AI Generated',
                    'name'  => 'is_ai_generated',
                    'type'  => 'true_false',
                ],
            ],
            'location' => [
                [['param' => 'post_type', 'operator' => '==', 'value' => 'market_report']],
            ],
        ]);
    }

