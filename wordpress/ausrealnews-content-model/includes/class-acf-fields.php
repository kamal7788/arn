                    'label'        => 'Bio',
                    'name'         => 'bio',
                    'type'         => 'textarea',
                    'instructions' => 'Agent biography.',
                    'required'     => 1,
                ],
                [
                    'key'          => 'field_service_area',
                    'label'        => 'Service Area',
                    'name'         => 'service_area',
                    'type'         => 'text',
                    'instructions' => 'Geographic area of expertise.',
                ],
                [
                    'key'          => 'field_agency_id',
                    'label'        => 'Agency',
                    'name'         => 'agency_id',
                    'type'         => 'relationship',
