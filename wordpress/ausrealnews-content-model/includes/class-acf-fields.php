<?php
/**
 * ACF Field Group Registration for Aus Real Estate News.
 *
 * Requires the Advanced Custom Fields (ACF) Pro plugin.
 * Registers field groups for articles, agent profiles, and agency profiles.
 */
class AusRealNews_ACF_Fields {

    public static function register(): void {
        if (!function_exists('acf_add_local_field_group')) {
            return;
        }

        self::register_article_fields();
        self::register_market_report_fields();
        self::register_agent_profile_fields();
        self::register_agency_profile_fields();
    }

    private static function register_article_fields(): void {
        acf_add_local_field_group([
            'key'      => 'group_ausrealnews_article',
            'title'    => 'Article Settings',
            'fields'   => [
                [
                    'key'          => 'field_source_urls',
                    'label'        => 'Source URLs',
                    'name'         => 'source_urls',
                    'type'         => 'url',
                    'instructions' => 'Research source URLs used for this article.',
                    'required'     => 0,
                    'multiple'     => true,
                    'wrapper'      => ['width' => '100'],
                ],
                [
                    'key'          => 'field_ai_pipeline_id',
                    'label'        => 'AI Pipeline ID',
                    'name'         => 'ai_pipeline_id',
                    'type'         => 'text',
                    'instructions' => 'n8n workflow/pipeline ID that generated this content.',
                    'required'     => 0,
                ],
                [
                    'key'          => 'field_risk_level',
                    'label'        => 'Risk Level',
                    'name'         => 'risk_level',
                    'type'         => 'select',
                    'choices'      => [
                        'Low'    => 'Low',
                        'Medium' => 'Medium',
                        'High'   => 'High',
                    ],
                    'default_value' => 'Low',
                    'required'      => 0,
                ],
                [
                    'key'          => 'field_is_ai_generated',
                    'label'        => 'Is AI Generated',
                    'name'         => 'is_ai_generated',
                    'type'         => 'true_false',
                    'instructions' => 'Whether this content was generated by AI.',
                    'default_value' => 0,
                    'required'      => 0,
                ],
            ],
            'location' => [
                [
                    [
                        'param'    => 'post_type',
                        'operator' => '==',
                        'value'    => 'post',
                    ],
                ],
                [
                    [
                        'param'    => 'post_type',
                        'operator' => '==',
                        'value'    => 'suburb_guide',
                    ],
                ],
                [
                    [
                        'param'    => 'post_type',
                        'operator' => '==',
                        'value'    => 'policy_update',
                    ],
                ],
            ],
            'position'              => 'normal',
            'style'                 => 'default',
            'label_placement'       => 'top',
            'instruction_placement' => 'label',
        ]);
    }

    private static function register_market_report_fields(): void {
